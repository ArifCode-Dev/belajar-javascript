// Logika / logical
// && , || , !
// AND , OR , NOT
x = 5;
console.log(x % 2 == 0);
x = 12;
console.log(x % 2 == 0);
console.log(x % 2 == 0 && x < 10); // duanya harus true
x = 4;
console.log(x % 2 == 0 && x < 10);
x = 12;
console.log(x % 2 == 0 || x < 10); // salah satu true
x = 10;
console.log(x < 20);
console.log(!(x < 20));

